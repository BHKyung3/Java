package ch01.ex04;

public class OperatorExample {

	public static void main(String[] args) {

		int a = 95786;
		int b = 654;
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		
		String ab = "apple";
		int c = 456;
		
		System.out.println(ab+c);
	
	}

}
